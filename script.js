const publicationsContainer = document.getElementById('publications-container');

if (publicationsContainer && Array.isArray(window.publications)) {
    window.publications.forEach(publication => {
        const titleText = publication.title || 'Untitled';
        const authorsText = publication.authors || '';

        const card = document.createElement('div');
        card.classList.add('publication-card');

        const title = document.createElement('h3');
        if (publication.url) {
            const link = document.createElement('a');
            link.href = publication.url;
            link.target = '_blank';
            link.rel = 'noopener';
            link.textContent = titleText;
            title.appendChild(link);
        } else {
            title.textContent = titleText;
        }

        const authors = document.createElement('p');
        authors.textContent = authorsText;

        const detailsParts = [];
        if (publication.journal) detailsParts.push(`<em>${publication.journal}</em>`);
        if (publication.volume) detailsParts.push(`vol. ${publication.volume}`);
        if (publication.number) detailsParts.push(`no. ${publication.number}`);
        if (publication.pages) detailsParts.push(`pp. ${publication.pages}`);
        if (publication.year) detailsParts.push(`${publication.year}`);

        const detailsParagraph = document.createElement('p');
        detailsParagraph.innerHTML = detailsParts.join(', ');

        card.appendChild(title);
        card.appendChild(authors);
        if (detailsParts.length) card.appendChild(detailsParagraph);

        publicationsContainer.appendChild(card);
    });
}

// Mobile menu toggle
document.addEventListener('DOMContentLoaded', function () {
    const mobileMenuToggle = document.querySelector('.mobile-menu-toggle');
    const navLinks = document.querySelector('.nav-links');

    if (mobileMenuToggle && navLinks) {
        mobileMenuToggle.addEventListener('click', function () {
            navLinks.classList.toggle('active');
            const icon = this.querySelector('i');
            if (icon) {
                if (navLinks.classList.contains('active')) {
                    icon.classList.remove('fa-bars');
                    icon.classList.add('fa-times');
                } else {
                    icon.classList.remove('fa-times');
                    icon.classList.add('fa-bars');
                }
            }
        });

        // Close menu when clicking on a link
        const navLinkItems = navLinks.querySelectorAll('a');
        navLinkItems.forEach(link => {
            link.addEventListener('click', function () {
                navLinks.classList.remove('active');
                const icon = mobileMenuToggle.querySelector('i');
                if (icon) {
                    icon.classList.remove('fa-times');
                    icon.classList.add('fa-bars');
                }
            });
        });
    }
});

// Dark Mode & Animations
document.addEventListener('DOMContentLoaded', function () {
    // Theme Toggle
    const themeToggle = document.getElementById('theme-toggle');
    const prefersDark = window.matchMedia('(prefers-color-scheme: dark)');

    // Check saved theme or system preference
    const savedTheme = localStorage.getItem('theme');
    if (savedTheme === 'dark' || (!savedTheme && prefersDark.matches)) {
        document.documentElement.setAttribute('data-theme', 'dark');
        if (themeToggle) themeToggle.innerHTML = '<i class="fa-solid fa-sun"></i>';
    }

    if (themeToggle) {
        themeToggle.addEventListener('click', () => {
            const currentTheme = document.documentElement.getAttribute('data-theme');
            const newTheme = currentTheme === 'dark' ? 'light' : 'dark';

            document.documentElement.setAttribute('data-theme', newTheme);
            localStorage.setItem('theme', newTheme);

            themeToggle.innerHTML = newTheme === 'dark'
                ? '<i class="fa-solid fa-sun"></i>'
                : '<i class="fa-solid fa-moon"></i>';
        });
    }

    // Scroll Animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                observer.unobserve(entry.target); // Only animate once
            }
        });
    }, observerOptions);

    document.querySelectorAll('.animate-on-scroll').forEach(el => {
        observer.observe(el);
    });
});